    private void btnDepExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepExcluirActionPerformed
        Dependente dep = new Dependente();
        DependenteController controller = new DependenteController();
        Integer pos = 0;
        for (Empregado e1 : listaEmp) {
            if (e1.getNome().equals(boxDepEmpregado.getSelectedItem())) {
                pos = listaEmp.indexOf(e1);
            }
        }
        dep.setCpfEmpregado(listaEmp.get(pos).getCpf());
        dep.setNome(txtDepNome.getText());
        Object[] opcoes = {"SIM", "Não"};
        int escolha
                = JOptionPane.showOptionDialog(null, "Deseja excluir: "
                        + txtDepNome.getText(), "Exclusão",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]);
        if (escolha == JOptionPane.YES_OPTION) {
            lblDepMsg.setText(controller.excluir(dep));
        }
        mostraDados();
    }//GEN-LAST:event_btnDepExcluirActionPerformed

