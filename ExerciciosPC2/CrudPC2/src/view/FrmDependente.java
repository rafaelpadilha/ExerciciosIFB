    private void btnDepInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepInserirActionPerformed
        Dependente dep = new Dependente();
        DependenteController controller = new DependenteController();
        Integer pos = 0;
        for (Empregado e1 : listaEmp) {
            if (e1.getNome().equals(boxDepEmpregado.getSelectedItem())) {
                pos = listaEmp.indexOf(e1);
            }
        }
        dep.setCpfEmpregado(listaEmp.get(pos).getCpf());
        dep.setNome(txtDepNome.getText());
        dep.setGrauParentesco(boxGrauParentesco.getSelectedItem().toString());
        SimpleDateFormat sfPostgres = new SimpleDateFormat("dd/MM/yyyy");
        String sDataNasc = fTxtData.getText();
        try {
            dep.setDataNascimento(sfPostgres.parse(sDataNasc));
        } catch (ParseException e) {
            lblDepMsg.setText(e.getMessage());
        }
        lblDepMsg.setText(controller.inserir(dep));
        mostraDados();
    }//GEN-LAST:event_btnDepInserirActionPerformed

