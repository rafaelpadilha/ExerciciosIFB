    private void tblDependentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDependentesMouseClicked
        Integer linha = tblDependentes.getSelectedRow();
        String cpfDependente = tblDependentes.getValueAt(linha, 0).toString();
        String nome = tblDependentes.getValueAt(linha, 1).toString();
        String grauParente = tblDependentes.getValueAt(linha, 2).toString();
        String dataNasc = tblDependentes.getValueAt(linha, 3).toString();

        txtDepNome.setText(nome);
        boxGrauParentesco.setSelectedItem(grauParente);
        fTxtData.setText(dataNasc);
        Integer pos = 0;

        for (Empregado e1 : listaEmp) {
            if (e1.getCpf().equals(tblDependentes.getValueAt(linha, 0).toString())) {
                pos = listaEmp.indexOf(e1);
            }
            boxDepEmpregado.setSelectedItem(listaEmp.get(pos).getNome());
        }
    }//GEN-LAST:event_tblDependentesMouseClicked

