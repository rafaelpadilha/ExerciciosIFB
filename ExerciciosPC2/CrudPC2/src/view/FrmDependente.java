    public void mostraDados() {
        List<Dependente> listaDep = new ArrayList<>();
        DependenteController controller = new DependenteController();
        listaDep = controller.listarTodos();
        DefaultTableModel tbm = (DefaultTableModel) tblDependentes.getModel();

        for (int i = tbm.getRowCount() - 1; i >= 0; i--) {
            tbm.removeRow(i);
        }
        int i = 0;
        for (Dependente dp : listaDep) {
            tbm.addRow(new String[1]);
            tblDependentes.setValueAt(dp.getCpfEmpregado(), i, 0);
            tblDependentes.setValueAt(dp.getNome(), i, 1);
            tblDependentes.setValueAt(dp.getGrauParentesco(), i, 2);
            tblDependentes.setValueAt(dp.getDataNascimento(), i, 3);
            i++;
        }
    }

